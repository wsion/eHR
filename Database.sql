CREATE TABLE Employee(
ID INT PRIMARY KEY IDENTITY(1,1),
Name NVARCHAR(10),
Phone NVARCHAR(11),
Sex BIT, --1: male ; 0: female
BirthDate DATETIME,
DepartmentID INT,
Note NVARCHAR(500),
CONSTRAINT FK_DEPT FOREIGN KEY (DepartmentId) REFERENCES Department (ID)
)
GO

